int main()
{

    double h,u,d,f;
    while(1){
       cin >>h>>u>>d>>f;
       if(!h)break;

       double x=(u/100)*f;
       double climb=0.0,up=(double)u,down=(double)d;
       int ans;
       bool done=0;
       for(int i=1;;i++){
          climb+=up;
          if(climb>h){ans=i;done=1;break;}
          else if(climb<0){ans=i;done=0;break;}
          climb-=down;
          if(climb<0){ans=i;done=0;break;}
          up-=x;
          if(up<0)up=0;
       }
       if(done)pf("success on day %d\n",ans);
       else pf("failure on day %d\n",ans);
    }
    return 0;
}








